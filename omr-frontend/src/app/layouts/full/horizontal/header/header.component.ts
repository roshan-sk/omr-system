@Component({
  selector: 'app-search-dialog',
  imports: [RouterModule, MaterialModule, TablerIconsModule, FormsModule],
  templateUrl: 'search-dialog.component.html',
})

export class AppHorizontalSearchDialogComponent {
  searchText: string = '';
  navItems = navItems;

  constructor(public authService: AuthService) {} // ← ADDED

  get navItemsData() {
    const term = this.searchText.toLowerCase().trim();
    const isAdmin = this.authService.currentUser?.role === 'ADMIN';
    return navItems.filter(
      (navitem) =>
        navitem.displayName &&
        (!navitem['adminOnly'] || isAdmin) &&
        (!term || navitem.displayName.toLowerCase().includes(term)),
    );
  }
}
